#define F_CPU 16000000UL

#include <avr/io.h>
#include <util/delay.h>

int main(void)
{
	DDRC = 0xFF;
	
	int num [10] = {0x3F, 0x30, 0x5B, 0x4F, 0x66, 0x6D, 0x7D, 0x27, 0x7F, 0x6F};
	int word[22] = {0x5F, 0x7C, 0x58, 0x5E, 0x7B, 0x71, 0x6F, 0x74, 0x30, 0x0E, 0x76, 0x38, 0x54, 0x5C, 0x73, 0x67, 0x50, 0x6D, 0x78, 0x1C, 0x66, 0x5B};
	int i;
	while (1)
	{
		for(i=0;i<10;i++)
		{
			PORTC = num[i];
			_delay_ms(500);
		}
		
		for(i=0;i<22;i++)
		{
			PORTC = word[i];
			_delay_ms(500);
		}
		
	}
}

